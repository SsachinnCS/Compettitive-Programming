ll gcd(ll a, ll b) {
    while (b) {
        a %= b;
        swap(a, b);
    }
    return a;
}

void solve() {
    ll a, b, k;
    cin >> a >> b >> k;
    ll g = gcd(a, b);
    ll dx = a / g;
    ll dy = b / g;
    if (max(dx, dy) <= k) cout << 1 << endl;
    else cout << 2 << endl;
}

